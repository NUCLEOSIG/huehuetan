                            <h4 align="center"><a href="#" class="post-headline mb-0">Comisión Municipal de los Derechos Humanos (CMDH)</a></h4>
                           <br>
							<h3>¿Qué es la CMDH?</h3>
 <p align="justify">Es un órgano del Ayuntamiento, encargado de la defensa, promoción, estudio y  divulgación de los derechos humanos reconocidos por la Constitución Mexicana, los tratados internacionales y las leyes estatales. La competencia de la comisión se extiende a las actividades de las autoridades municipales o de cualquier persona que preste servicios públicos concesionados por el Ayuntamiento. </p>
                <br>
							
							<h3>¿Qué es una queja?</h3>
                
               
<p align="justify">Es el procedimiento al que puede recurrir una persona cuando sus derechos humanos, o los de otra, han sido violados, principalmente los relativos a la vida, a la integridad física y dignidad, a la libertad personal, a la seguridad jurídica, a la igualdad ante la ley, a la libertad de pensamiento, conciencia y de religión, a la libertad de expresión y de opinión, entre otros. El objetivo es la restitución plena del goce de los derechos del agraviado y la reparación, en la medida de lo posible, de los daños que se le hayan ocasionado.</p>  
				
				
							<h4 align="center">¡CMDH!</h4>
							
<p align="justify">
Los Derechos Humanos son el conjunto de prerrogativas sustentadas en la dignidad humana, cuya realización efectiva resulta indispensable para el desarrollo integral de la persona. Este conjunto de prerrogativas se encuentra establecido dentro del orden jurídico nacional, en nuestra Constitución Política, tratados internacionales y las leyes estatales.<br>

El respeto hacia los derechos humanos de cada persona es un deber de todos. Todas las autoridades en el ámbito de sus competencias, tienen la obligación de promover, respetar, proteger y garantizar los derechos humanos consignados en favor del individuo.<br>

De igual manera, la aplicación de los derechos humanos a la que se encuentran obligadas todas las autoridades se rige por los principios de universalidad, interdependencia, indivisibilidad y progresividad.<br><br>

<strong>Principio de Universalidad,</strong> señala que los derechos humanos corresponden a todas las personas por igual.<br>

<strong>Principio de Interdependencia:</strong> consiste en que cada uno de los derechos humanos se encuentran ligados unos a otros, de tal manera que el reconocimiento de uno de ellos, así como su ejercicio, implica necesariamente que se respeten y protejan múltiples derechos que se encuentran vinculados.<br>

<strong>Principio de Indivisibilidad:</strong> Se habla de indivisibilidad de los derechos humanos en función a que poseen un carácter indivisible pues todos ellos son inherentes al ser humano y derivan de su dignidad.<br>

Lo anterior quiere decir que disfrute de los derechos humanos sólo es posible en conjunto y no de manera aislada ya que todos se encuentran estrechamente unidos.<br>

<strong>Principio de Progresividad:</strong> Constituye una obligación del Estado para asegurar el progreso en el desarrollo constructivo de los derechos humanos, al mismo tiempo, implica una prohibición para el Estado respecto a cualquier retroceso de los derechos.<br><br>

El Estado debe proveer las condiciones más óptimas de disfrute de los derechos y no disminuir ese nivel logrado.<br>

El poder público debe hacer todo lo necesario para que sean superadas la desigualdad, la pobreza y la discriminación.<br>

La promoción, respeto, protección y garantía de los Derechos Humanos tiene la función de:<br>

    Contribuir al desarrollo integral de la persona.<br>
    Buscar que todas las personas gocen de una esfera de autonomía donde les sea posible trazar un plan de vida digna que pueda ser desarrollado, protegidas de los abusos de autoridades, servidores públicos y de los mismos particulares.<br>
    Representa límites a las actuaciones de todos los servidores públicos, sin importar su nivel jerárquico o institución gubernamental, sea Federal, Estatal o Municipal, siempre con el fin de prevenir los abusos de poder, negligencia o simple desconocimiento de la función.<br>
    Crear condiciones suficientes que permitan a todas las personas tomar parte activa en el manejo de los asuntos públicos y en la adopción de las decisiones comunitarias (vida democrática).<br>

<strong>Clasificación de los derechos humanos</strong><br>

Los Derechos Humanos han sido clasificados atendiendo a diversos criterios, así podemos encontrar clasificaciones que atienden a su naturaleza, al origen, contenido y por la materia a la que se refieren. Con un propósito pedagógico han sido clasificados en tres generaciones, esto en función al momento histórico en que surgieron o del reconocimiento que han tenido por parte de los Estados.<br>

Es conveniente indicar que el agrupamiento de los derechos humanos en generaciones no significa que algunos tengan mayor o menor importancia sobre otros pues todos ellos encuentran en la dignidad humana el principio y fin a alcanzar. Así entonces en la primera generación fueron agrupados los derechos civiles y políticos, en la segunda generación los derechos económicos, sociales y culturales y en la tercera generación se agruparon los que corresponden a grupos de personas o colectividades que comparten intereses comunes.<br>

Actualmente es mayormente aceptado clasificar los derechos humanos únicamente en civiles, económicos, sociales, culturales y ambientales.<br>

Es importante decir que dentro del conjunto de derechos humanos no existen niveles ni jerarquías pues todos tienen igual relevancia, por lo que el Estado se encuentra obligado a tratarlos en forma global y de manera justa y equitativa, en pie de igualdad y dándoles a todos el mismo peso.<br>

