<p align="justify">La tradición histórica refiere que el pueblo de Huehuetán, el más antiguo del Soconusco, fue fundado por el caudillo Balum Votán a su paso cuando iba a Centroamérica. En 1540, se le erige en capital de la provincia del Soconusco, ostentando tal rango hasta 1700 en que, por su decadencia económica, pasan las autoridades a residir en el pueblo de Escuintla. En 1792, provisionalmente se asientan las autoridades provinciales, pero ahora como cabecera de la subdelegación del Soconusco de la Intendencia de Chiapas, en sustitución de Escuintla que fue destruida por una tromba marina, fungiendo como tal hasta el 23 de mayo de 1794 en que la subdelegación pasa al pueblo de Tapachula. En 1800, una fuerte creciente del río Huehuetán semidestruyó al pueblo arruinándolo. Fue hasta 1882, después de la firma del tratado de límites entre México y Guatemala, cuando se reactiva la vida económica y política del municipio, al ser realmente reconocidas y valoradas sus autoridades municipales.</p>  
				
				
<p align="justify">
